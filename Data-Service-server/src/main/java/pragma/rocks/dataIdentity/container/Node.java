package pragma.rocks.dataIdentity.container;

public class Node {
	private int id;
	private String label;

	public Node() {

	}

	public Node(int id, String label) {
		this.id = id;
		this.label = label;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
}
